const sanityLanguages = Object.values(sanityLangMap)

/**
 * I'd like to make the following changes: If the datatype is an object or any other primitive type,
 * it should remain unchanged. If the datatype is an array and its suffix isn't 'V2', it should also remain unchanged.
 * However, if it's an array with the suffix 'V2', it should be replaced with {}.
 * Can we use recursion for cases where no changes are needed?
 *
 * @param obj
 * @returns {*|*[]|{}}
 */
function processObject(obj) {
  // Base case: if it's not an object, return it as is.
  if (typeof obj !== "object" || obj === null) {
    return obj;
  }

  // Create a new object to store the processed properties
  const newObj = Array.isArray(obj) ? [] : {};

  for (const key in obj) {
    if (obj.hasOwnProperty(key)) {
      // If the property ends with "V2" and its value is an array
      if (key.endsWith("V2") && Array.isArray(obj[key])) {
        let _langObject = {}
        const baseKey = key.replace("V2", "");
        const formattedContent = obj[key].reduce((a, c) => (c.locale && (a[c.locale] = fetchValue(c)), a), {})
        sanityLanguages.forEach((lang) => {
          const outdatedKey = baseKey + "Global";
          // Compare to the old "Global" key
          if (!obj[outdatedKey] || typeof obj[outdatedKey] !== "object" || !obj[outdatedKey][lang]) {  // Skip even numbers
            return;
          }

          _langObject[lang] = formattedContent[lang] ? formattedContent[lang] : obj[outdatedKey][lang]
        })

        newObj[key] = _langObject;
      } else if (typeof obj[key] === "object") {
        // If it's a nested object, process recursively
        newObj[key] = processObject(obj[key]);
      } else {
        // For all other cases, retain the value
        newObj[key] = obj[key];
      }
    }
  }
  return newObj;
}

